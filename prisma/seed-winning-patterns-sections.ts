// ─────────────────────────────────────────────────────────────────
// Idempotent upsert — (sourceProject, sectionKey) 조합 기준
// ─────────────────────────────────────────────────────────────────

async function upsertSectionRecord(
  core: CaseCore,
  section: SectionRecord,
): Promise<'created' | 'updated'> {
  const normalizedProfile = normalizeProfile(buildProfile(core.profileCore))
  const sourceProfileJson = normalizedProfile as unknown as Prisma.InputJsonValue

  const existing = await prisma.winningPattern.findFirst({
    where: {
      sourceProject: core.sourceProject,
      sectionKey: section.sectionKey,
    },
    select: { id: true },
  })

  const data = {
    sourceProject: core.sourceProject,
    sourceClient: core.sourceClient,
    sectionKey: section.sectionKey,
    channelType: core.channelType,
    outcome: core.outcome,
    techEvalScore: null,
    snippet: section.snippet,
    whyItWorks: section.whyItWorks,
    tags: section.tags,
    sourceProfile: sourceProfileJson,
  }

  if (existing) {
    await prisma.winningPattern.update({ where: { id: existing.id }, data })
    return 'updated'
  }

  await prisma.winningPattern.create({ data })
  return 'created'
}

async function main() {
  const totalRecords = CASE_SECTION_BUNDLES.reduce(
    (sum, b) => sum + b.sections.length,
    0,
  )

  console.log('🌱 섹션별 WinningPattern 시드 시작...')
  console.log(
    `   (${CASE_SECTION_BUNDLES.length} 케이스 × 평균 ${Math.round(
      totalRecords / CASE_SECTION_BUNDLES.length,
    )} 섹션 = 총 ${totalRecords} 레코드)\n`,
  )

  let createdCount = 0
  let updatedCount = 0

  for (const bundle of CASE_SECTION_BUNDLES) {
    const mark = bundle.estimated ? '🟡 추정' : '🟢 원문'
    console.log(`  ${mark} ${bundle.core.sourceProject}`)
    for (const section of bundle.sections) {
      const action = await upsertSectionRecord(bundle.core, section)
      if (action === 'created') createdCount++
      else updatedCount++
      const marker = action === 'created' ? '    ✓ 신규' : '    ↻ 갱신'
      console.log(`  ${marker}  [${section.sectionKey}]`)
    }
  }

  console.log(
    `\n✅ 완료 — 신규 ${createdCount}건 · 갱신 ${updatedCount}건 (총 ${totalRecords}건)`,
  )
}

