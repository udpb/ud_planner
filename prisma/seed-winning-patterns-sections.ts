/**
 * 6축 core + defensible defaults → 완전한 ProgramProfile.
 * normalizeProfile() 통과 후 저장된다.
 */
function buildProfile(core: CaseCore['profileCore']): ProgramProfile {
  return {
    targetStage: core.targetStage,
    targetSegment: {
      demographic: ['무관'],
      businessDomain: [core.businessDomain0],
      geography: '일반',
    },
    scale: {
      budgetKrw: 300_000_000,
      budgetTier: '1-3억', // normalizeProfile 이 재계산
      participants: '20-50',
      durationMonths: 6,
    },
    formats: [],
    delivery: {
      mode: '하이브리드',
      usesLMS: true,
      onlineRatio: 30,
      usesAICoach: false,
    },
    supportStructure: {
      tasks: core.tasks,
      fourLayerSupport: false,
      coachingStyle: '1:1',
      externalSpeakers: true,
    },
    methodology: {
      primary: core.methodologyPrimary,
      impactModulesUsed: [],
    },
    selection: {
      style: '서류+PT',
      stages: 2,
      competitionRatio: '중간_1:3-5',
      publicVoting: false,
      evaluatorCount: 5,
    },
    channel: {
      type: core.channelType,
      clientTier: core.clientTier,
      isRenewal: false,
    },
    primaryImpact: [core.primaryImpact0],
    aftercare: {
      hasAftercare: true,
      scope: ['alumni네트워크'],
      tierCount: 1,
    },
    version: '1.0',
    updatedAt: UPDATED_AT,
  }
}

