type SectionKey = 'curriculum' | 'coaches' | 'budget' | 'impact' | 'other'

// ─────────────────────────────────────────────────────────────────
// 입력 타입
// ─────────────────────────────────────────────────────────────────

interface CaseCore {
  sourceProject: string
  sourceClient: string
  channelType: ChannelType
  outcome: 'won' | 'lost' | 'pending'
  /** sourceProfile 에 담을 lightweight 7축 (v1.1: tasks 추가) */
  profileCore: {
    targetStage: TargetStage
    businessDomain0: BusinessDomain
    methodologyPrimary: MethodologyPrimary
    channelType: ChannelType
    clientTier: ClientTier
    primaryImpact0: PrimaryImpact
    tasks: ProjectTaskType[]
  }
}

interface SectionRecord {
  sectionKey: SectionKey
  snippet: string
  whyItWorks: string
  tags: string[]
}

interface CaseSectionBundle {
  core: CaseCore
  /** 원문 미확보 케이스용 플래그. 헤더 주석으로만 남김. */
  estimated?: boolean
  sections: SectionRecord[]
}

const UPDATED_AT = new Date().toISOString()

